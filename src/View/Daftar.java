    private void btnDaftarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDaftarActionPerformed
        String id = txtID.getText().toString().trim();
        String username = txtUsername.getText().toString().trim();
        String password = txtPassword.getText().toString().trim();
        String conpassword = txtConPass.getText().toString().trim();
        
        if(!password.equals(conpassword)){
            JOptionPane.showMessageDialog(null, "Password tidak sama!");
        }else if(password.equals("") || username.equals("")){
            JOptionPane.showMessageDialog(null, "Username dan Password mohon diisi!");
        }else{
            PreparedStatement statement = null;
            try{
                Connection con = (Connection) ConnectionDB.getConnection();
                String sql = "INSERT INTO login (id, username, password, conpassword) VALUES (?,?,?,?)";
                statement = con.prepareStatement(sql);
//                PreparedStatement p = (PreparedStatement) con.prepareStatement(sql);
                statement.setString(1, id);
                statement.setString(2, username);
                statement.setString(3, password);
                statement.setString(4, conpassword);
                statement.execute();
                statement.close();
                JOptionPane.showMessageDialog(null, "Pembuatan akun berhasil");
            }catch(SQLException e){
                System.out.println("Error");
            }finally{
                this.dispose();
                Login a = new Login();
                a.setVisible(true);
            }
        }
    }//GEN-LAST:event_btnDaftarActionPerformed

