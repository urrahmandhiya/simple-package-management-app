    private void autonumber() {
        try{
            Connection con = (Connection) ConnectionDB.getConnection();
            Statement st = (Statement) con.createStatement();
            String sql = "SELECT * FROM login ORDER BY id DESC";
            ResultSet r = st.executeQuery(sql);
            if (r.next()) {
                String NoID = r.getString("id").substring(4);
                String ID = "" +(Integer.parseInt(NoID)+1);
                String Zero = "";
                
                if (ID.length()==1){
                    Zero = "00";
                }else if(ID.length()==2){
                    Zero = "0";
                }else if(ID.length()==3){
                    Zero = "";
                }
                
                txtID.setText("USER" + Zero + ID);
            }else{
                txtID.setText("USER001");
            }
            r.close();
            st.close();
        }catch(Exception e){
            System.out.println("autonumber error");
        }
    }
